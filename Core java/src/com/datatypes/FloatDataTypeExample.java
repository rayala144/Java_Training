package com.datatypes;

public class FloatDataTypeExample {
	public static void main(String[] args) {
		float a, b, x, y;
		a = Float.MAX_VALUE;
		b = Float.MIN_VALUE;
		x = 3.46f;
		y = 6.12f;
		System.out.println("a value is:\t"+ a + ":\t b value is:\t" + b);
		System.out.println("x value is:\t"+ x + ":\t y value is:\t" + y);
	}
}
